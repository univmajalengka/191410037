#include <iostream>
#include <string>
using namespace std;

int main(){
int a[4][4][2] = {{{1,8},{2,7},{3,6},(4,5)} ,
		 {{5,4},{6,3},{7,2},(8,1)} ,
		 {{1,8},{2,7},{3,6},(4,5)} ,
		 {{5,4},{6,3},{7,2},(8,1)}};
int b[4][4][2] = {{{8,1},{7,2},{6,3},(5,4)} ,
		 {{4,5},{3,6},{2,7},(1,8)} ,
		 {{8,1},{7,2},{6,3},(5,4)} ,
		 {{4,5},{3,6},{2,7},(1,8)}};
int c[4][4][2];

	for(int i=0;i<4;i++){
		for(int j=0;j<4;j++)
			for(int k=0;k<2;k++){
			c[i][j][k]=a[i][j][k]+b[i][j][k];
		}
	}
	
	for(int i=0;i<4;i++){
		for(int j=0;j<4;j++)
			for(int k=0;k<2;k++){
			cout << c[i][j][k]<< " ";
		}
		cout << endl;
	}
}
